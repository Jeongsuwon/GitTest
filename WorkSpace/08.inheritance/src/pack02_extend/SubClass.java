package pack02_extend;

public class SubClass extends SuperClass{
	//두 수의 곱하기 return, 나누기 return, 나누고 나머지 return 하는 메소드 3개를 구현해보기
	
	public int gop(int x, int y) {
		return x*y;
		
	}
	public int nanugi(int x, int y) {
		return x-y;
		
	}
	public int namuzi(int x, int y) {
		return x%y;
		
	}
	
}
