package pack02_Byte;

public class Ex01_Byte {

	public static void main(String[] args) {
		//{ } <= main 지역
		//byte 타입의 변수를 선언해보기 var1
		
		//※변수는 표현 범위를 가지기 때문에 해당하는 범위 안에서 사용!
		byte var1;
		var1 = 0;
		byte var2 = 10;
		
		var1 = -128;
		var2 = 127;
		
		System.out.println(var1);
		System.out.println(var2);

	}

}
