
public class Ex05_Oper {

	public static void main(String[] args) {
		int result = 0;
		// result에 10을 더한 결과를 다시 할당.
		// 최종적으로 20
		
		result = result + 10; // result += 10;
		result = result + 10; // result += 10;		
		result += 10;
		result += 10; // 40
		
		System.out.println(result+10); // 50
		System.out.println(result); // 40
		
	}

}
